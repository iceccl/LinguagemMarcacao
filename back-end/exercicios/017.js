const adicao = (v1, v2) => {
    let calculo = v1 + v2;
    return calculo;
}

const subtracao = (v1, v2) => {
    let calculo = v1 - v2;
    return calculo;
}

const multiplicacao = (v1, v2) => {
    let calculo = v1 * v2;
    return calculo;
}

const divisao = (v1, v2) => {
    let calculo = v1 / v2;
    return calculo;
}

console.log("10 + 10 = ",adicao(10, 10))
console.log("10 - 10 = ",subtracao(10, 10))
console.log("10 * 10 = ",multiplicacao(10, 10))
console.log("10 / 10 = ",divisao(10, 10))